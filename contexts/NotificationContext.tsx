import AsyncStorage from "@react-native-async-storage/async-storage";
import { useFocusEffect } from "@react-navigation/native";
import React, { createContext, useCallback, useContext, useEffect, useRef, useState } from "react";
import { io, Socket } from "socket.io-client";
import { baseURL } from "../services/api.config";
import {
  getNotifications,
  Notification,
} from "../services/notification.service";

const SOCKET_URL = `${baseURL}/`;

interface NotificationContextType {
  // Notification list by type
  notificationsUser: Notification[];
  notificationsActivity: Notification[];
  notificationsSystem: Notification[];
  hasUnreadNotification: boolean;
  refreshNotifications: () => void;
  refreshNotificationsByType: (type: 'user' | 'activity' | 'system') => void;
  
  // Socket management
  reconnectSocket: () => void;
  
  // Toast notification
  showToast: (title: string, message: string) => void;
  hideToast: () => void;
  toastVisible: boolean;
  toastTitle: string;
  toastMessage: string;
}

const NotificationContext = createContext<NotificationContextType | undefined>(undefined);

export const useNotificationContext = () => {
  const context = useContext(NotificationContext);
  if (!context) {
    throw new Error("useNotificationContext must be used within NotificationProvider");
  }
  return context;
};

export const NotificationProvider: React.FC<{ children: React.ReactNode }> = ({
  children,
}) => {
  // Notification list state by type
  const [notificationsUser, setNotificationsUser] = useState<Notification[]>([]);
  const [notificationsActivity, setNotificationsActivity] = useState<Notification[]>([]);
  const [notificationsSystem, setNotificationsSystem] = useState<Notification[]>([]);
  const [hasUnreadNotification, setHasUnreadNotification] = useState(false);
  const [userId, setUserId] = useState<string | null>(null);
  const [userToken, setUserToken] = useState<string | null>(null);
  
  // Toast state
  const [toastVisible, setToastVisible] = useState(false);
  const [toastTitle, setToastTitle] = useState("");
  const [toastMessage, setToastMessage] = useState("");
  
  // Socket
  const [socket, setSocket] = useState<Socket | null>(null);
  const socketRef = useRef<Socket | null>(null);
  const isConnectingRef = useRef(false);

  // K·∫øt n·ªëi socket ƒë·ªÉ nh·∫≠n realtime notifications
  useEffect(() => {
    const initializeSocket = async () => {
      try {
        const [currentUserId, currentToken] = await Promise.all([
          AsyncStorage.getItem("userId"),
          AsyncStorage.getItem("token"),
        ]);

        // N·∫øu ƒëang k·∫øt n·ªëi, kh√¥ng l√†m g√¨
        if (isConnectingRef.current) {
          return;
        }

        // N·∫øu user ho·∫∑c token thay ƒë·ªïi, disconnect socket c≈©
        if (socketRef.current && (currentUserId !== userId || currentToken !== userToken)) {
          console.log("üîÑ User changed, disconnecting old socket...");
          socketRef.current.disconnect();
          socketRef.current = null;
          setSocket(null);
        }

        if (currentUserId && currentToken) {
          setUserId(currentUserId);
          setUserToken(currentToken);
          
          // Ch·ªâ t·∫°o socket m·ªõi n·∫øu ch∆∞a c√≥ ho·∫∑c user thay ƒë·ªïi
          if (!socketRef.current || currentUserId !== userId || currentToken !== userToken) {
            console.log("üîÑ Creating new socket for user:", currentUserId);
            isConnectingRef.current = true;
            
            const newSocket = io(SOCKET_URL, {
              transports: ["websocket"],
              auth: { token: `Bearer ${currentToken}` },
              reconnection: true,
              reconnectionAttempts: 5,
              reconnectionDelay: 1000,
              timeout: 20000,
            });

            newSocket.on("connect", () => {
              console.log("‚úÖ Notification socket connected for user:", currentUserId);
              isConnectingRef.current = false;
            });

            newSocket.on("disconnect", () => {
              console.log("‚ùå Notification socket disconnected for user:", currentUserId);
              isConnectingRef.current = false;
            });

            newSocket.on("connect_error", (error) => {
              console.error("‚ùå Notification socket connection error:", error);
              isConnectingRef.current = false;
            });

            // L·∫Øng nghe notification m·ªõi
            newSocket.on("new_notification", (notification: Notification) => {
              // Ki·ªÉm tra xem notification c√≥ d√†nh cho user hi·ªán t·∫°i kh√¥ng
              if (notification.receivers?.includes(currentUserId)) {
                // Th√™m notification v√†o ƒë√∫ng category
                switch (notification.type) {
                  case "user":
                    setNotificationsUser(prev => [notification, ...prev]);
                    break;
                  case "activity":
                    setNotificationsActivity(prev => [notification, ...prev]);
                    break;
                  case "system":
                    setNotificationsSystem(prev => [notification, ...prev]);
                    break;
                }
                
                // C·∫≠p nh·∫≠t hasUnreadNotification
                setHasUnreadNotification(true);
                
                // Hi·ªÉn th·ªã toast
                showToast(notification.title, notification.content);
              }
            });

            newSocket.emit("join", currentUserId);
            socketRef.current = newSocket;
            setSocket(newSocket);
          }
        } else {
          // N·∫øu kh√¥ng c√≥ user ho·∫∑c token, disconnect socket
          if (socketRef.current) {
            console.log("üîÑ No user/token, disconnecting socket...");
            socketRef.current.disconnect();
            socketRef.current = null;
            setSocket(null);
          }
          setUserId(null);
          setUserToken(null);
        }
      } catch (error) {
        console.error("‚ùå Error initializing notification socket:", error);
        isConnectingRef.current = false;
      }
    };

    initializeSocket();

    // Cleanup khi component unmount
    return () => {
      if (socketRef.current) {
        socketRef.current.disconnect();
        socketRef.current = null;
      }
    };
  }, []); // Ch·ªâ ch·∫°y m·ªôt l·∫ßn khi mount

  // T√≠nh to√°n hasUnreadNotification d·ª±a tr√™n t·∫•t c·∫£ notifications v√† userId
  useEffect(() => {
    if (!userId) return;

    const allNotifications = [
      ...notificationsUser,
      ...notificationsActivity,
      ...notificationsSystem
    ];

    const hasUnread = allNotifications.some(
      (n) => !n.isReadBy || !n.isReadBy.includes(userId)
    );
    setHasUnreadNotification(hasUnread);
  }, [notificationsUser, notificationsActivity, notificationsSystem, userId]);

  const fetchAllNotifications = async () => {
    try {
      const token = await AsyncStorage.getItem("token");
      if (!token) return;
      
      const [user, activity, system] = await Promise.all([
        getNotifications({ type: "user", token }),
        getNotifications({ type: "activity", token }),
        getNotifications({ type: "system", token }),
      ]);
      
      setNotificationsUser(user.data || []);
      setNotificationsActivity(activity.data || []);
      setNotificationsSystem(system.data || []);
    } catch (error) {
      console.error("‚ùå Error fetching notifications:", error);
    }
  };

  const refreshNotificationsByType = async (type: 'user' | 'activity' | 'system') => {
    try {
      const token = await AsyncStorage.getItem("token");
      if (!token) return;
      
      const response = await getNotifications({ type, token });
      
      switch (type) {
        case "user":
          setNotificationsUser(response.data || []);
          break;
        case "activity":
          setNotificationsActivity(response.data || []);
          break;
        case "system":
          setNotificationsSystem(response.data || []);
          break;
      }
    } catch (error) {
      console.error(`‚ùå Error fetching ${type} notifications:`, error);
    }
  };

  useEffect(() => {
    fetchAllNotifications();
  }, []);

  // Toast functions
  const showToast = (title: string, message: string) => {
    setToastTitle(title);
    setToastMessage(message);
    setToastVisible(true);
  };

  const hideToast = () => {
    setToastVisible(false);
  };

  const reconnectSocket = () => {
    // Disconnect socket hi·ªán t·∫°i
    if (socketRef.current) {
      console.log("üîÑ Force disconnecting socket...");
      socketRef.current.disconnect();
      socketRef.current = null;
      setSocket(null);
    }
    
    // Reset state
    setUserId(null);
    setUserToken(null);
    isConnectingRef.current = false;
    
    // Kh·ªüi t·∫°o l·∫°i socket sau m·ªôt ch√∫t
    setTimeout(() => {
      const initializeSocket = async () => {
        try {
          const [currentUserId, currentToken] = await Promise.all([
            AsyncStorage.getItem("userId"),
            AsyncStorage.getItem("token"),
          ]);

          if (currentUserId && currentToken) {
            console.log("üîÑ Reconnecting socket for user:", currentUserId);
            isConnectingRef.current = true;
            
            const newSocket = io(SOCKET_URL, {
              transports: ["websocket"],
              auth: { token: `Bearer ${currentToken}` },
              reconnection: true,
              reconnectionAttempts: 5,
              reconnectionDelay: 1000,
              timeout: 20000,
            });

            newSocket.on("connect", () => {
              console.log("‚úÖ Notification socket reconnected for user:", currentUserId);
              isConnectingRef.current = false;
            });

            newSocket.on("disconnect", () => {
              console.log("‚ùå Notification socket disconnected for user:", currentUserId);
              isConnectingRef.current = false;
            });

            newSocket.on("connect_error", (error) => {
              console.error("‚ùå Notification socket connection error:", error);
              isConnectingRef.current = false;
            });

            // L·∫Øng nghe notification m·ªõi
            newSocket.on("new_notification", (notification: Notification) => {
              if (notification.receivers?.includes(currentUserId)) {
                switch (notification.type) {
                  case "user":
                    setNotificationsUser(prev => [notification, ...prev]);
                    break;
                  case "activity":
                    setNotificationsActivity(prev => [notification, ...prev]);
                    break;
                  case "system":
                    setNotificationsSystem(prev => [notification, ...prev]);
                    break;
                }
                
                setHasUnreadNotification(true);
                showToast(notification.title, notification.content);
              }
            });

            newSocket.emit("join", currentUserId);
            socketRef.current = newSocket;
            setSocket(newSocket);
            setUserId(currentUserId);
            setUserToken(currentToken);
          }
        } catch (error) {
          console.error("‚ùå Error reconnecting notification socket:", error);
          isConnectingRef.current = false;
        }
      };
      
      initializeSocket();
    }, 1000); // Delay 1 gi√¢y tr∆∞·ªõc khi reconnect
  };

  // Reconnect socket khi app ƒë∆∞·ª£c focus l·∫°i
  useFocusEffect(
    useCallback(() => {
      const checkAndReconnect = async () => {
        try {
          const [currentUserId, currentToken] = await Promise.all([
            AsyncStorage.getItem("userId"),
            AsyncStorage.getItem("token"),
          ]);

          // N·∫øu c√≥ user v√† token nh∆∞ng socket kh√¥ng k·∫øt n·ªëi, reconnect
          if (currentUserId && currentToken && (!socketRef.current || !socketRef.current.connected)) {
            console.log("üîÑ App focused, reconnecting socket...");
            reconnectSocket();
          }
        } catch (error) {
          console.error("‚ùå Error checking socket on focus:", error);
        }
      };

      checkAndReconnect();
    }, [])
  );

  // Ki·ªÉm tra socket connection ƒë·ªãnh k·ª≥
  useEffect(() => {
    const interval = setInterval(async () => {
      try {
        const [currentUserId, currentToken] = await Promise.all([
          AsyncStorage.getItem("userId"),
          AsyncStorage.getItem("token"),
        ]);

        // N·∫øu c√≥ user v√† token nh∆∞ng socket kh√¥ng k·∫øt n·ªëi, reconnect
        if (currentUserId && currentToken && (!socketRef.current || !socketRef.current.connected)) {
          console.log("üîÑ Socket disconnected, reconnecting...");
          reconnectSocket();
        }
      } catch (error) {
        console.error("‚ùå Error checking socket connection:", error);
      }
    }, 30000); // Ki·ªÉm tra m·ªói 30 gi√¢y

    return () => clearInterval(interval);
  }, []);

  const value: NotificationContextType = {
    // Notification list by type
    notificationsUser,
    notificationsActivity,
    notificationsSystem,
    hasUnreadNotification,
    refreshNotifications: fetchAllNotifications,
    refreshNotificationsByType,
    
    // Socket management
    reconnectSocket,
    
    // Toast notification
    showToast,
    hideToast,
    toastVisible,
    toastTitle,
    toastMessage,
  };

  return (
    <NotificationContext.Provider value={value}>
      {children}
    </NotificationContext.Provider>
  );
};
